export function t(key: string, lang?: Lang): string {
  const l = lang || useI18n.getState().lang
  return (l === 'zh-CN' ? zh : en)[key] || key
}

/** 带占位符的翻译，如 tf('common.total', { n: 5 }) */
export function tf(key: string, vars: Record<string, string | number>, lang?: Lang): string {
  let s = t(key, lang)
  for (const [k, v] of Object.entries(vars)) {
    s = s.replace(`{${k}}`, String(v))
  }
  return s
}

export function useT() {
  const lang = useI18n((s) => s.lang)
  return useMemo(
    () => (key: string) => (lang === 'zh-CN' ? zh : en)[key] || key,
    [lang],
  )
}

